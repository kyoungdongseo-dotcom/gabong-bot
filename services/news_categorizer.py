def categorize(title: str, summary: str) -> str:
    text = (title or "") + " " + (summary or "")
    for cat, kws in CATEGORIES.items():
        if not kws:
            continue
        if any(kw in text for kw in kws):
            return cat
    return "📰 기타 정보"


def score_news(title: str, description: str = "") -> int:
    text_original = (title or "") + " " + (description or "")
    score = 0
    for kw, points in PRIORITY_KEYWORDS.items():
        if kw in text_original:
            score += points
    for kw, penalty in EXCLUDE_KEYWORDS.items():
        if kw in text_original:
            score += penalty
    return score
